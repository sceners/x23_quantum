double                                  floor( double x )
{
    //------------------------------

    int v;
    if ( x>0 )
    {
        x += 0.5;
        v = ( int )x;
        v--;
    }
    else
    {
        x -= 0.5;
        v = ( int )x;
    }
    return ( double )v;

    //------------------------------
}

